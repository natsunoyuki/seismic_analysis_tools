# ymaeda_tools

# This set of tools are meant to be used with the seismic inversion programs written by Dr. Yuta Maeda of Nagoya university.
# The original set of programs are referred to as YMAEDA_TOOLS which is written in C.

# Python files
# ------------
# 1. dft.py
#    Functions associated with the discrete Fourier transform functions used in YMAEDA_TOOLS.
# 2. inversion_data.py
#    Functions associated with creation of the frequency space data used during linear inversion by YMAEDA_TOOLS.
# 3. inversion.py
#    Functions associated with the linear inversion algorithm used in YMAEDA_TOOLS.
# 4. polezero.py
#    Functions associated with poles and zeros (seismometer response) used in YMAEDA_TOOLS.
# 5. read_dGM.py
#    Functions associated with reading the d, G and M .seq files created by YMAEDA_TOOLS.
# 6. read_snapshot.py
#    Functions associated with reading the "effective Green's functions" snapshot .3db files created by YMAEDA_TOOLS.
# 7. read_stfun.py
#    Functions associated with the source time functions used in YMAEDA_TOOLS.